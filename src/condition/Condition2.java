package condition;
// #4-2  조건문2(2024-02-08)
public class Condition2 {
    public static void main(String[] args) {
        // 변수 설정
        int age = 10;

        // 조건문
        if (age >= 18){

            System.out.println("성인입니다.");       // 조건이 true인 경우 수행

        }else {

            System.out.println("미성년자입니다.");     // 조건이 false인 경우 수행

        }
    }
}
