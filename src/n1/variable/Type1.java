package n1.variable;
//#2-5 변수 선언 타입(2024-02-07)
public class Type1 {
    public static void main(String[] args) {
        // 정수
        int a = 100;

        // 실수
        double b = 20.5;

        // 참-거짓 논리 값
        boolean c = true;

        // 문자 하나, 홑따옴표
        char d = 'A';

        // 문자열, 쌍따옴표
        String e = "Hi!";
    }
}
